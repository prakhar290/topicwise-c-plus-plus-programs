#include<iostream>
using namespace std;
int main()
{
    // for taking input we use 'cin' followed by extraction operator '>>'.
    int num1;
    cout<<"enter number 1: ";
    cin>>num1;
    int num2;
    cout<<"enter number 2: ";
    cin>>num2;
    int sum;
    sum = num1 + num2;
    cout<<"sum of two number is: ";
    cout<<sum;
    return 0;
}