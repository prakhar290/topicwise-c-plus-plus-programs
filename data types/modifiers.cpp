#include<iostream>
using namespace std;
int main()
{
    //modifiers are used to increase the range of int/float datatypes
    unsigned int ui;
    signed int si;
    long int li;
    short int shi;
    cout<<"the size of ui is: "<<sizeof(ui)<<endl;
    cout<<"the size of si is: "<<sizeof(si)<<endl;
    cout<<"the size of li is: "<<sizeof(li)<<endl;
    cout<<"the size of shi is: "<<sizeof(shi)<<endl;
    return 0;
}